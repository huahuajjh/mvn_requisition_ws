package com.tq.entity;

public class FileType
{
  private String id;
  private String title;
  private String pid;

  public String getId()
  {
    return this.id;
  }
  public void setId(String id) {
    this.id = id;
  }
  public String getTitle() {
    return this.title;
  }
  public void setTitle(String title) {
    this.title = title;
  }
  public String getPid() {
    return this.pid;
  }
  public void setPid(String pid) {
    this.pid = pid;
  }
}